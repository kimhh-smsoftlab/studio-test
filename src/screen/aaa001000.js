function getRandomIntInclusive(min, max) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min + 1)) + min; //최댓값도 포함, 최솟값도 포함
}

function getRandomFloatInclusive(min, max) {
  return (Math.random() * (max - min + 1)) + min; //최댓값도 포함, 최솟값도 포함
}



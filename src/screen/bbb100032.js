// [form:event ] OnFormInit  start...
form.OnFormInit=function(tagName, objData){
    gb_all.$html.show();
    gb_time.$html.hide();
    gb_invest.$html.hide();
    gb_1.$html.hide();
};
// [tab:event ] OnTabChanged  start...
tab_1.OnTabChanged=function(nCurrentTab){
    if (nCurrentTab == 1) {
        gb_all.$html.show();
        gb_time.$html.hide();
        gb_invest.$html.hide();
        gb_1.$html.hide();
    } else if (nCurrentTab == 2) {
        gb_all.$html.hide();
        gb_time.$html.show();
        gb_invest.$html.hide();
        gb_1.$html.hide();
    } else if (nCurrentTab == 3) {
        gb_all.$html.hide();
        gb_time.$html.show();
        gb_invest.$html.hide();
        gb_1.$html.hide();
    } else if (nCurrentTab == 4) {
        gb_all.$html.hide();
        gb_time.$html.hide();
        gb_invest.$html.show();
        gb_1.$html.hide();
    }  else if (nCurrentTab == 5) {
        gb_all.$html.hide();
        gb_time.$html.hide();
        gb_invest.$html.hide();
        gb_1.$html.show();
    }
    
};